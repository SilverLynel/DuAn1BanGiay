    private void chonKhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chonKhachHangActionPerformed
        // TODO add your handling code here:
        int row = tbKhachHang.getSelectedRow();
        if (row != -1) {
            int selectedRow = tbKhachHang.getSelectedRow();
            String ma = tbKhachHang.getValueAt(selectedRow, 0) + "";
            for (KhachHang k : lstKhachHang) {
                if (k.getMaKH().equals(ma)) {
                    _banHang.getKhachHang(k);
                    this.setVisible(false);
                }
            }
        }
    }//GEN-LAST:event_chonKhachHangActionPerformed

