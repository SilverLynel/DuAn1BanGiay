    private void loadTableKhachHang() {
        lstKhachHang = khachHangService.getList();
        model.setRowCount(0);
        for (KhachHang k : lstKhachHang) {
            model.addRow(new Object[]{
                    k.getMaKH(),
                    k.getHoTen(),
                    k.isGioiTinh() + "" == "" ? "" : (k.isGioiTinh() == true ? "Nam" : "Nữ"),
                    k.getDiaChi(),
                    k.getSdt(),
                    k.getEmail(),
                    k.isTrangThai() + "" == "" ? "" : (k.isTrangThai() == true ? "Hoạt động" : "Ngừng hoạt động"),
                    k.getGhiChu()
            });
        }
    }

